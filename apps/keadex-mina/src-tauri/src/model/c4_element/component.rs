/*!
Model representing a C4 Component element.
*/

use crate::core::serializer::format_with_indent;
use crate::model::{
  c4_element::base_element::BaseElement, diagram::diagram_plantuml::PlantUMLSerializer,
};
use crate::parser::plantuml::plantuml_parser::Rule;
use bomboni_wasm::Wasm;
use pest::error::Error;
use pest::iterators::Pair;
use serde::{Deserialize, Serialize};
use std::str::FromStr;
use strum_macros::{Display, EnumString};
use ts_rs::TS;
use wasm_bindgen::prelude::wasm_bindgen;

#[derive(TS)]
#[ts(
  export,
  export_to = "../../../libs/c4-model-ui-kit/src/models/autogenerated/"
)]
#[derive(Serialize, Deserialize, Display, Debug, EnumString, Clone, Wasm)]
#[wasm(wasm_abi)]
pub enum ComponentType {
  #[serde(rename = "Component")]
  #[strum(serialize = "Component")]
  Component,
  #[serde(rename = "Component_Ext")]
  #[strum(serialize = "Component_Ext")]
  ComponentExt,
  #[serde(rename = "ComponentDb")]
  #[strum(serialize = "ComponentDb")]
  ComponentDb,
  #[serde(rename = "ComponentDb_Ext")]
  #[strum(serialize = "ComponentDb_Ext")]
  ComponentDbExt,
  #[serde(rename = "ComponentQueue")]
  #[strum(serialize = "ComponentQueue")]
  ComponentQueue,
  #[serde(rename = "ComponentQueue_Ext")]
  #[strum(serialize = "ComponentQueue_Ext")]
  ComponentQueueExt,
}

#[derive(TS)]
#[ts(
  export,
  export_to = "../../../libs/c4-model-ui-kit/src/models/autogenerated/"
)]
#[derive(Serialize, Deserialize, Debug, Default, Clone)]
#[wasm_bindgen(getter_with_clone)]
pub struct Component {
  pub base_data: BaseElement,
  #[serde(skip_serializing_if = "Option::is_none")]
  pub technology: Option<String>,
  #[serde(skip_serializing_if = "Option::is_none")]
  pub component_type: Option<ComponentType>,
}

impl<'i> TryFrom<Pair<'i, Rule>> for Component {
  type Error = Error<Rule>;

  fn try_from(pair: Pair<Rule>) -> Result<Self, Self::Error> {
    let mut component = Self::default();

    let cloned_pair = pair.clone();
    component.base_data = cloned_pair.try_into().unwrap();

    let items = pair.into_inner();
    for item in items {
      match item.as_rule() {
        Rule::stdlib_c4_container_component_types => {
          component.component_type = Some(ComponentType::from_str(item.as_str()).unwrap())
        }
        Rule::stdlib_c4_techn => {
          component.technology = Some(item.into_inner().next().unwrap().as_str().to_string())
        }
        _ => {}
      }
    }

    Ok(component)
  }
}

impl PlantUMLSerializer for Component {
  fn serialize_to_plantuml(&self, level: usize) -> String {
    let mut component_ser = String::new();
    // Serialize "component type"
    if let Some(component_type) = &self.component_type {
      component_ser.push_str(&format!("{}(", component_type));
    }
    // Serialize "alias"
    if let Some(alias) = &self.base_data.alias {
      component_ser.push_str(&format!("{}, ", alias));
    }
    // Serialize "label"
    if let Some(label) = &self.base_data.label {
      component_ser.push_str(&format!("\"{}\"", label));
    }
    // Serialize "technology"
    if let Some(technology) = &self.technology {
      component_ser.push_str(&format!(", \"{}\"", technology));
    }
    // Serialize "description"
    if let Some(description) = &self.base_data.description {
      component_ser.push_str(&format!(", \"{}\"", description));
    }
    // Serialize "sprite"
    if let Some(sprite) = &self.base_data.sprite {
      component_ser.push_str(&format!(", $sprite=\"{}\"", sprite));
    }
    // Serialize "tags"
    if let Some(tags) = &self.base_data.tags {
      component_ser.push_str(&format!(", $tags=\"{}\"", tags));
    }
    // Serialize "link"
    if let Some(link) = &self.base_data.link {
      component_ser.push_str(&format!(", $link=\"{}\"", link));
    }
    component_ser.push_str(")");

    format_with_indent(level, component_ser)
  }
}

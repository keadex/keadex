import {
  COMPONENT_TYPES,
  Component,
  ComponentType,
  parseTags,
} from '@keadex/c4-model-ui-kit'
import {
  Input,
  Select,
  TagsInput,
  Textarea,
  renderButtons,
} from '@keadex/keadex-ui-kit/cross'
import { capitalCase, noCase } from 'change-case'
import { useEffect, useState } from 'react'
import { useTranslation } from 'react-i18next'
import { toast } from 'react-toastify'
import { v4 as uuidv4 } from 'uuid'
import {
  ALIAS_REGEX,
  NAME_EXTENDED_REGEX,
  NAME_REGEX,
} from '../../../constants/regex'
import { useAppDispatch } from '../../../core/store/hooks'
import { saveProject } from '../../../core/store/slices/project-slice'
import {
  createLibraryElement,
  updateLibraryElement,
} from '../../../core/tauri-rust-bridge'
import { MinaError } from '../../../models/autogenerated/MinaError'
import { Project } from '../../../models/autogenerated/Project'
import DiagramLinker from '../../DiagramLinker/DiagramLinker'
import {
  ModalCRULibraryElementProps,
  normalizeLibraryElement,
  onTagsChanged,
} from '../ModalCRULibraryElements'
import { twMerge } from 'tailwind-merge'

const emptyComponent: Component = {
  base_data: {
    uuid: uuidv4(),
    alias: '',
    label: '',
    description: '',
    notes: '',
  },
  technology: '',
}

export const ModalCRUComponent = (props: ModalCRULibraryElementProps) => {
  const { t } = useTranslation()
  const dispatch = useAppDispatch()
  const [newComponent, setNewComponent] = useState(
    props.libraryElement ? (props.libraryElement as Component) : emptyComponent,
  )
  const [isLoading, setIsLoading] = useState(false)

  function handleUpdateComponent() {
    setIsLoading(true)
    updateLibraryElement(
      { Component: props.libraryElement as Component },
      { Component: normalizeLibraryElement(newComponent) },
    )
      .then((updatedProjectLibrary) => {
        setIsLoading(false)
        if (props.project?.project_settings && props.project?.project_library) {
          const newProject: Project = {
            ...props.project,
            project_library: updatedProjectLibrary,
          }
          dispatch(saveProject(newProject))
          toast.info(t('common.info.done'))
          if (props.forceUpdate) props.forceUpdate()
          props.hideModal()
        }
      })
      .catch((error: MinaError) => {
        setIsLoading(false)
        toast.error(error.msg ?? error)
      })
  }

  function handleCreateComponent() {
    setIsLoading(true)
    createLibraryElement({ Component: normalizeLibraryElement(newComponent) })
      .then((updatedProjectLibrary) => {
        setIsLoading(false)
        if (props.project?.project_settings && props.project?.project_library) {
          const newProject: Project = {
            ...props.project,
            project_library: updatedProjectLibrary,
          }
          dispatch(saveProject(newProject))
          toast.info(t('common.info.done'))
          if (props.forceUpdate) props.forceUpdate()
          props.hideModal()
        }
      })
      .catch((error: MinaError) => {
        setIsLoading(false)
        toast.error(error.msg ?? error)
      })
  }

  function missingRequiredFields() {
    return (
      !newComponent?.base_data?.alias ||
      !newComponent?.base_data?.label ||
      !newComponent.component_type
    )
  }

  useEffect(() => {
    emptyComponent.base_data.uuid = uuidv4()
  }, [])

  return (
    <div>
      {/* Modal body */}
      <div className="modal__body">
        <Input
          disabled={true}
          type="text"
          label={`UUID`}
          className="mt-2"
          value={newComponent?.base_data?.uuid}
        />
        <Input
          disabled={props.libraryElement !== undefined || !props.enableEdit}
          type="text"
          label={`${t('common.alias')}*`}
          className="mt-6"
          allowedChars={ALIAS_REGEX}
          info={`${t('common.allowed_pattern')}: ${ALIAS_REGEX}`}
          value={newComponent?.base_data?.alias}
          onChange={(e) =>
            setNewComponent({
              ...newComponent,
              base_data: {
                ...newComponent?.base_data,
                alias: e.target.value.replace(' ', ''),
              },
            })
          }
        />
        <Input
          disabled={!props.enableEdit}
          type="text"
          label={`${t('common.label')}*`}
          className="mt-6"
          allowedChars={NAME_REGEX}
          info={`${t('common.allowed_pattern')}: ${NAME_REGEX}`}
          value={newComponent?.base_data?.label}
          onChange={(e) =>
            setNewComponent({
              ...newComponent,
              base_data: {
                ...newComponent?.base_data,
                label: e.target.value,
              },
            })
          }
        />
        <Select
          id="type-selector"
          disabled={!props.enableEdit}
          label={`${t('common.type')}*`}
          className="mt-6"
          value={newComponent?.component_type}
          options={[{ label: '', value: '' }].concat([
            ...new Set(
              COMPONENT_TYPES.map((componentType) => {
                return {
                  label: capitalCase(noCase(componentType)),
                  value: componentType,
                }
              }),
            ),
          ])}
          onChange={(e) =>
            setNewComponent({
              ...newComponent,
              component_type: e.target.value as ComponentType,
            })
          }
        />
        <Input
          disabled={!props.enableEdit}
          type="text"
          label={`${t('common.technology')}`}
          className="mt-6"
          allowedChars={NAME_EXTENDED_REGEX}
          info={`${t('common.allowed_pattern')}: ${NAME_EXTENDED_REGEX}`}
          value={newComponent?.technology ?? ''}
          onChange={(e) =>
            setNewComponent({
              ...newComponent,
              technology: e.target.value,
            })
          }
        />
        <TagsInput
          id="modal-cru-component-tags"
          disabled={!props.enableEdit}
          className="mt-6 cursor-text"
          label={t('common.tags')}
          tags={parseTags(newComponent.base_data.tags) ?? []}
          settings={{
            callbacks: {
              add: (e) => onTagsChanged(e, setNewComponent),
              remove: (e) => onTagsChanged(e, setNewComponent),
            },
            maxTags: 5,
            editTags: false,
          }}
        />
        <Textarea
          id="component-description"
          disabled={!props.enableEdit}
          label={t('common.description')}
          className="mt-6"
          value={newComponent?.base_data?.description}
          onChange={(e) =>
            setNewComponent({
              ...newComponent,
              base_data: {
                ...newComponent?.base_data,
                description: e.target.value,
              },
            })
          }
        />
        <DiagramLinker
          hideButtons
          className="p-0"
          disabled={!props.enableEdit}
          link={newComponent?.base_data?.link}
          onLinkChanged={(link) => {
            setNewComponent({
              ...newComponent,
              base_data: {
                ...newComponent?.base_data,
                link,
              },
            })
          }}
        />
        {props.mode !== 'serializer' && (
          <Textarea
            id="component-note"
            disabled={!props.enableEdit}
            label={`${t('common.notes')}`}
            className="mt-6"
            value={newComponent?.base_data?.notes}
            onChange={(e) =>
              setNewComponent({
                ...newComponent,
                base_data: {
                  ...newComponent?.base_data,
                  notes: e.target.value,
                },
              })
            }
          />
        )}
        <span
          className={twMerge(
            `text-sm text-orange-500 mt-2`,
            missingRequiredFields() ? 'block' : 'hidden',
          )}
        >
          * {t('common.info.fill_required_fields')}
        </span>
      </div>

      {/* Modal footer */}
      <div className="modal__footer">
        {renderButtons([
          {
            key: 'button-cancel',
            disabled: isLoading,
            children: <span>{t('common.cancel')}</span>,
            'data-te-modal-dismiss': true,
          },
          {
            key: 'button-save',
            children: <span>{t('common.save')}</span>,
            disabled: missingRequiredFields(),
            isLoading,
            onClick: () => {
              if (props.mode === 'serializer') {
                if (props.onElementCreated) {
                  props.onElementCreated({
                    Component: normalizeLibraryElement(newComponent),
                  })
                  props.hideModal()
                }
              } else if (props.project) {
                if (
                  props.libraryElement &&
                  props.mode !== 'createLibraryElement'
                ) {
                  handleUpdateComponent()
                } else {
                  handleCreateComponent()
                }
              }
            },
          },
        ])}
      </div>
    </div>
  )
}

export default ModalCRUComponent

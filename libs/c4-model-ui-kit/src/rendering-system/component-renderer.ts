import { Component } from '../models/autogenerated/Component'
import { DiagramSpec } from '../models/autogenerated/DiagramSpec'
import C4Component from '../components/c4-component/C4Component'
import C4ComponentExt from '../components/c4-component/C4ComponentExt'
import { getElementSpecByAlias } from '../helper/diagram-helper'
import { RenderElementsOptions } from './diagram-renderer'
import { C4BaseComponent } from '../components/C4BaseComponent'
import { ElementData } from '../models/autogenerated/ElementData'

export const renderComponentDiagramElement = (
  canvas: fabric.Canvas | undefined,
  component: Component,
  diagramSpec: DiagramSpec,
  autoLayout: Record<string, ElementData>,
  options?: RenderElementsOptions,
): C4BaseComponent | undefined => {
  if (
    component.base_data &&
    component.base_data.alias &&
    component.component_type &&
    ((diagramSpec.auto_layout_enabled &&
      autoLayout[component.base_data.alias]) ||
      !diagramSpec.auto_layout_enabled)
  ) {
    let c4Component: C4BaseComponent | undefined
    switch (component.component_type) {
      case 'Component':
      case 'ComponentDb':
      case 'ComponentQueue':
        c4Component = C4Component(
          component,
          getElementSpecByAlias(
            component.base_data.alias,
            undefined,
            undefined,
            { Component: component.component_type },
            diagramSpec,
          ),
          autoLayout,
          undefined,
          options,
        )
        break
      case 'Component_Ext':
      case 'ComponentDb_Ext':
      case 'ComponentQueue_Ext':
        c4Component = C4ComponentExt(
          component,
          getElementSpecByAlias(
            component.base_data.alias,
            undefined,
            undefined,
            { Component: component.component_type },
            diagramSpec,
          ),
          autoLayout,
          undefined,
          options,
        )
        break
    }
    if ((!options || !options.skipAddToCanvas) && c4Component)
      canvas?.add(c4Component)
    return c4Component
  }
  return
}

export default renderComponentDiagramElement

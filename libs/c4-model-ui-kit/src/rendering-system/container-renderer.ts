import { Container } from '../models/autogenerated/Container'
import { DiagramSpec } from '../models/autogenerated/DiagramSpec'
import C4Container from '../components/c4-container/C4Container'
import C4ContainerExt from '../components/c4-container/C4ContainerExt'
import { getElementSpecByAlias } from '../helper/diagram-helper'
import { RenderElementsOptions } from './diagram-renderer'
import { C4BaseComponent } from '../components/C4BaseComponent'
import { ElementData } from '../models/autogenerated/ElementData'

export const renderContainerDiagramElement = (
  canvas: fabric.Canvas | undefined,
  container: Container,
  diagramSpec: DiagramSpec,
  autoLayout: Record<string, ElementData>,
  options?: RenderElementsOptions,
): C4BaseComponent | undefined => {
  if (
    container.base_data &&
    container.base_data.alias &&
    container.container_type &&
    ((diagramSpec.auto_layout_enabled &&
      autoLayout[container.base_data.alias]) ||
      !diagramSpec.auto_layout_enabled)
  ) {
    let c4Container: C4BaseComponent | undefined
    switch (container.container_type) {
      case 'Container':
      case 'ContainerDb':
      case 'ContainerQueue':
        c4Container = C4Container(
          container,
          getElementSpecByAlias(
            container.base_data.alias,
            undefined,
            undefined,
            { Container: container.container_type },
            diagramSpec,
          ),
          autoLayout,
        )
        break
      case 'Container_Ext':
      case 'ContainerDb_Ext':
      case 'ContainerQueue_Ext':
        c4Container = C4ContainerExt(
          container,
          getElementSpecByAlias(
            container.base_data.alias,
            undefined,
            undefined,
            { Container: container.container_type },
            diagramSpec,
          ),
          autoLayout,
        )
        break
    }
    if ((!options || !options.skipAddToCanvas) && c4Container)
      canvas?.add(c4Container)
    return c4Container
  }
  return
}

export default renderContainerDiagramElement

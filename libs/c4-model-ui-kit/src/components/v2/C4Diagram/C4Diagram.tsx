export const C4Diagram = forwardRef(
  (props: C4DiagramProps, ref: Ref<C4DiagramCommmands>) => {
    const [nodes, setNodes, onNodesChange] = useNodesState<C4DiagramNode>([])
    const [edges, setEdges, onEdgesChange] = useEdgesState<C4DiagramEdge>([])
    const diagramListener = useRef<DiagramListener | null>(null)
    const [autoLayoutEnabled, setAutoLayoutEnabled] = useState(true)
    const [autoLayoutOrientation, setAutoLayoutOrientation] = useState<
      DiagramOrientation | undefined
    >('TopToBottom')
    const [backgroundColor, setBackgroundColor] = useState(
      DIAGRAM.COLOR.BG_COLOR,
    )
    const [gridEnabled, setGridEnabled] = useState(false)

    //---- Start Ref implementation
    function setDiagramListener(newDiagramListener: DiagramListener) {
      diagramListener.current = newDiagramListener
    }

    useImperativeHandle(ref, () => ({
      setDiagramListener,
      setAutoLayoutEnabled,
      setAutoLayoutOrientation,
      setBackgroundColor,
      setGridEnabled,
      setNodes,
      setEdges,
    }))
    //---- End Ref implementation

    const onConnect = useCallback(
      (params: Connection) => setEdges((eds) => addEdge(params, eds)),
      [setEdges],
    )

    return (
      <ReactFlow
        colorMode="light"
        nodes={nodes}
        edges={edges}
        onNodesChange={onNodesChange}
        onEdgesChange={onEdgesChange}
        onConnect={onConnect}
        style={{ backgroundColor }}
      >
        <MiniMap />
        <Controls />
        <Background variant={BackgroundVariant.Dots} />
      </ReactFlow>
    )
  },
)

export default C4Diagram

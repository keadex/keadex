'use client'

import '@xyflow/react/dist/style.css'

import {
  addEdge,
  Background,
  BackgroundVariant,
  Connection,
  Controls,
  Edge as EdgeType,
  MiniMap,
  Node as NodeType,
  ReactFlow,
  useEdgesState,
  useNodesState,
} from '@xyflow/react'
import {
  forwardRef,
  Ref,
  useCallback,
  useImperativeHandle,
  useRef,
  useState,
} from 'react'

import { DiagramOrientation } from '../../../models/autogenerated/DiagramOrientation'
import { DIAGRAM } from '../../../styles/style-constants'
import { DiagramListener } from '../../C4BaseComponent'

export type C4DiagramNode = NodeType & {}
export type C4DiagramEdge = EdgeType & {}

// eslint-disable-next-line @typescript-eslint/no-empty-object-type
export type C4DiagramProps = {}

export type C4DiagramCommmands = {
  setDiagramListener: (diagramListener: DiagramListener) => void
  setAutoLayoutEnabled: (enabled: boolean) => void
  setAutoLayoutOrientation: (orientation?: DiagramOrientation) => void
  setBackgroundColor: (color: string) => void
  setGridEnabled: (enabled: boolean) => void
  setNodes: (nodes: C4DiagramNode[]) => void
  setEdges: (edges: C4DiagramEdge[]) => void
}


import { Container } from '../../models/autogenerated/Container'
import { DiagramElementSpec } from '../../models/autogenerated/DiagramElementSpec'
import { ElementData } from '../../models/autogenerated/ElementData'
import { RenderElementsOptions } from '../../rendering-system/diagram-renderer'
import { BOX } from '../../styles/style-constants'
import C4BaseBox from '../C4BaseBox'
import { C4BaseComponent, C4BaseComponentOptions } from '../C4BaseComponent'
import C4BaseDb from '../C4BaseDb'
import { C4BaseQueue } from '../C4BaseQueue'

export const C4Container = (
  data: Container,
  elementSpec: DiagramElementSpec,
  autoLayout: Record<string, ElementData>,
  options: Partial<C4BaseComponentOptions> | undefined,
  renderElementsOptions: RenderElementsOptions | undefined,
): C4BaseComponent | undefined => {
  const defaultOptions: C4BaseComponentOptions = {
    bgColor:
      renderElementsOptions?.diagramsThemeSettings?.bg_color_container ??
      BOX.COLORS.BG_COLOR_CONTAINER,
    borderColor:
      renderElementsOptions?.diagramsThemeSettings?.border_color_container ??
      BOX.COLORS.BORDER_COLOR_CONTAINER,
    textColor:
      renderElementsOptions?.diagramsThemeSettings?.text_color_container ??
      BOX.COLORS.TEXT_COLOR_CONTAINER,
  }
  const enrichedOptions: C4BaseComponentOptions = {
    ...defaultOptions,
    ...options,
  }
  if (data.container_type) {
    switch (data.container_type) {
      case 'Container':
        return C4BaseBox(
          data,
          elementSpec,
          autoLayout,
          enrichedOptions,
          renderElementsOptions,
        )
      case 'ContainerDb':
        return C4BaseDb(
          data,
          elementSpec,
          autoLayout,
          enrichedOptions,
          renderElementsOptions,
        )
      case 'ContainerQueue':
        return C4BaseQueue(
          data,
          elementSpec,
          autoLayout,
          enrichedOptions,
          renderElementsOptions,
        )
    }
  }
  return
}

export default C4Container
